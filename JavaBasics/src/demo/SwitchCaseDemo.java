package demo;

public class SwitchCaseDemo {
	
	public static void main(String[] args) {
		String day = "Wednesday";
		
		switch(day) {
		case "Monday":
			System.out.println("Wear a white dress today");
			break;
			
		case "Tuesday":
			System.out.println("Wear a yellow dress today");
			break;
			
		case "Wednesday":
			System.out.println("Wear a orange dress today");
			break;
			
		case "Thursday":
			System.out.println("Wear a brown dress today");
			break;
			
		case "Friday":
			System.out.println("Wear a green dress today");
			break;
			
		case "Saturday":
			System.out.println("Wear a red dress today");
			break;
			
		case "Sunday":
			System.out.println("Wear a black dress today");
			break;
		
			
			
			
		}
	}

}
